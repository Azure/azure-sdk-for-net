#!/usr/bin/env pwsh
<#
.SYNOPSIS
    Runs the ModelReaderWriter proxy-resolution benchmarks and (re)generates a single
    consolidated Markdown results file from BenchmarkDotNet's GitHub exporter output.

.DESCRIPTION
    BenchmarkDotNet already emits a GitHub-flavored Markdown report per benchmark class
    ('*-report-github.md') under 'BenchmarkDotNet.Artifacts/results'. This script runs the
    proxy benchmarks, collects those reports, and writes them into one committed-friendly
    Markdown file with a header describing when/how it was generated.

    Run it any time you want to refresh the numbers.

.PARAMETER Filter
    BenchmarkDotNet '--filter' glob. Defaults to all proxy benchmarks.

.PARAMETER Framework
    Target framework to run the benchmarks on. Defaults to net8.0.

.PARAMETER OutputFile
    Path of the consolidated Markdown file to write.

.EXAMPLE
    ./Run-ProxyBenchmarks.ps1
    Runs every *ProxyResolution* benchmark and writes ModelReaderWriter/ProxyResolutionResults.md.

.EXAMPLE
    ./Run-ProxyBenchmarks.ps1 -Filter '*ProxyResolutionRealistic*'
    Runs only the realistic-model benchmarks (faster).
#>
[CmdletBinding()]
param(
    [string]$Filter = '*ProxyResolution*',
    [string]$Framework = 'net8.0',
    [string]$OutputFile = (Join-Path $PSScriptRoot 'ModelReaderWriter/ProxyResolutionResults.md')
)

$ErrorActionPreference = 'Stop'

$perfProject = Join-Path $PSScriptRoot 'System.ClientModel.Tests.Internal.Perf.csproj'
$artifactsDir = Join-Path $PSScriptRoot 'BenchmarkDotNet.Artifacts'
$resultsDir = Join-Path $artifactsDir 'results'

# Start from a clean slate so we only aggregate reports from this run.
if (Test-Path $artifactsDir) {
    Remove-Item $artifactsDir -Recurse -Force
}

Write-Host "Running proxy benchmarks (filter: $Filter, framework: $Framework)..." -ForegroundColor Cyan
dotnet run --project $perfProject -c Release --framework $Framework -- --filter $Filter --artifacts $artifactsDir
if ($LASTEXITCODE -ne 0) {
    throw "Benchmark run failed with exit code $LASTEXITCODE."
}

# BenchmarkDotNet writes one '<Type>-report-github.md' per benchmark class.
$reportFiles = @(Get-ChildItem -Path $resultsDir -Filter '*-report-github.md' -ErrorAction SilentlyContinue | Sort-Object Name)
if ($reportFiles.Count -eq 0) {
    # Fall back to any markdown report in case the GitHub exporter name changes.
    $reportFiles = @(Get-ChildItem -Path $resultsDir -Filter '*-report*.md' -ErrorAction SilentlyContinue | Sort-Object Name)
}
if ($reportFiles.Count -eq 0) {
    throw "No BenchmarkDotNet markdown reports were found in '$resultsDir'."
}

$sb = [System.Text.StringBuilder]::new()
[void]$sb.AppendLine('# MRW Proxy Resolution - Benchmark Results')
[void]$sb.AppendLine()
[void]$sb.AppendLine("> Generated by ``Run-ProxyBenchmarks.ps1`` on $(Get-Date -Format 'yyyy-MM-dd HH:mm') (local time).")
[void]$sb.AppendLine("> Filter: ``$Filter`` &middot; Framework: ``$Framework``")
[void]$sb.AppendLine('>')
[void]$sb.AppendLine('> These numbers are a point-in-time snapshot from the machine that ran the script and')
[void]$sb.AppendLine('> depend on the configured BenchmarkDotNet toolchain. Re-run the script to refresh them.')
[void]$sb.AppendLine('>')
[void]$sb.AppendLine('> The proxy benchmarks run **out-of-process** (the default BenchmarkDotNet toolchain) for')
[void]$sb.AppendLine('> measurement isolation; the rest of the perf suite stays on the fast in-process toolchain.')
[void]$sb.AppendLine('>')
[void]$sb.AppendLine('> **How to read these numbers.** The **Allocated** column is deterministic (identical across')
[void]$sb.AppendLine('> runs) and is the most reliable measure of proxy overhead. For timing, these read/write paths')
[void]$sb.AppendLine('> are sub-microsecond, so the **Median** is a better reference than the Mean, which a rare')
[void]$sb.AppendLine('> multi-microsecond GC/scheduling spike can inflate. Absolute timings of these sub-microsecond')
[void]$sb.AppendLine('> paths carry meaningful run-to-run variance on a non-isolated dev machine - in particular the')
[void]$sb.AppendLine('> read variants deserialize the same payload, so their timing differences are usually within')
[void]$sb.AppendLine('> noise and only the Allocated column (+48 B) is a reliable read-side signal. Treat timings as')
[void]$sb.AppendLine('> directional and rely on the Median and Allocated columns.')
[void]$sb.AppendLine()

foreach ($file in $reportFiles) {
    [void]$sb.AppendLine((Get-Content $file.FullName -Raw).TrimEnd())
    [void]$sb.AppendLine()
}

# ── Group the headline comparison at the top of the results table ──
# BenchmarkDotNet owns the row order of the joined summary, so it does not keep together the three
# rows that matter most: the realistic deserialize path with no proxy, a conditional proxy that
# declines (miss), and one that handles (hit). Re-float those three rows to the top of the table and
# add a short pointer note so the headline comparison stays grouped every time this is regenerated.
# If a benchmark is renamed and a row can't be matched, BenchmarkDotNet's order is left untouched.
$lines = @($sb.ToString().TrimEnd() -split "`r?`n")

# The three headline rows, in the order they should appear (no-proxy -> miss -> hit).
$headlinePatterns = @(
    '^\|\s*ProxyResolutionRealisticBenchmark\s*\|\s*Read_NoProxy\s*\|',
    '^\|\s*ProxyResolutionRealisticBenchmark\s*\|\s*Read_ConditionalProxy_Miss\s*\|',
    '^\|\s*ProxyResolutionRealisticBenchmark\s*\|\s*Read_ConditionalProxy_Hit\s*\|'
)

$headlineRows = @(foreach ($pattern in $headlinePatterns) {
    $lines | Where-Object { $_ -match $pattern } | Select-Object -First 1
})

$headerIdx = -1
for ($i = 0; $i -lt $lines.Count; $i++) {
    if ($lines[$i] -match '^\|\s*Type\s*\|\s*Method\s*\|') { $headerIdx = $i; break }
}

if ($headerIdx -ge 0 -and $headlineRows.Count -eq $headlinePatterns.Count) {
    $note = @(
        '**Headline comparison (top three rows).** The same realistic deserialize path with no proxy, a',
        'proxy that declines (miss, so the base model handles it), and a proxy that handles it (hit). The',
        'payload is identical across all three, so the no-proxy &rarr; miss &rarr; hit deltas are directly',
        'comparable. The remaining rows (micro variants, the unconditional proxy, and collection cases)',
        'are kept below for reference.'
    )

    $result = [System.Collections.Generic.List[string]]::new()
    for ($i = 0; $i -lt $lines.Count; $i++) {
        $line = $lines[$i]

        # Drop the headline rows from their original positions (re-added after the separator below).
        $isHeadline = $false
        foreach ($pattern in $headlinePatterns) {
            if ($line -match $pattern) { $isHeadline = $true; break }
        }
        if ($isHeadline) { continue }

        # Inject the note immediately before the table header.
        if ($i -eq $headerIdx) {
            $result.Add('')
            foreach ($n in $note) { $result.Add($n) }
            $result.Add('')
        }

        $result.Add($line)

        # Splice the headline rows in right after the header separator row.
        if ($i -eq ($headerIdx + 1)) {
            foreach ($row in $headlineRows) { $result.Add($row) }
        }
    }

    $finalContent = ($result -join [Environment]::NewLine).TrimEnd()
}
else {
    if ($headerIdx -lt 0) {
        Write-Warning 'Could not locate the results table header; leaving BenchmarkDotNet row order unchanged.'
    }
    else {
        Write-Warning 'Did not find all three headline rows; leaving BenchmarkDotNet row order unchanged.'
    }
    $finalContent = $sb.ToString().TrimEnd()
}

$outDir = Split-Path $OutputFile -Parent
if (-not (Test-Path $outDir)) {
    New-Item -ItemType Directory -Path $outDir -Force | Out-Null
}
Set-Content -Path $OutputFile -Value $finalContent -Encoding utf8

Write-Host "Wrote consolidated results to $OutputFile" -ForegroundColor Green
Write-Host "Aggregated $($reportFiles.Count) report(s)." -ForegroundColor Green
