using System;
using System.Collections.Generic;
using System.ComponentModel;
using System.Linq;
using System.Net;

// Backward-compat constructor and property shims for TypeSpec migration.

namespace Azure.ResourceManager.ContainerInstance.Models
{
    public partial class ContainerGroupIPAddress
    {
        /// <summary> Initializes a new instance of <see cref="ContainerGroupIPAddress"/>. </summary>
        /// <param name="ports"> The ports. </param>
        /// <param name="type"> The IP address type. </param>
        [EditorBrowsable(EditorBrowsableState.Never)]
        public ContainerGroupIPAddress(IEnumerable<ContainerGroupPort> ports, ContainerGroupIPAddressType type)
            : this(ports?.ToList(), type, default, default, default, default, default)
        {
        }

        // Property renames: Type→AddressType, Ip→IP (System.Net.IPAddress), AutoGeneratedDomainNameLabelScope→DnsNameLabelReusePolicy

        /// <summary> Specifies if the IP is exposed to the public internet or private VNET. </summary>
        [EditorBrowsable(EditorBrowsableState.Never)]
        public ContainerGroupIPAddressType AddressType { get => Type; set => Type = value; }

        /// <summary> The IP exposed to the public internet. </summary>
        [EditorBrowsable(EditorBrowsableState.Never)]
        public IPAddress IP
        {
            get => Ip != null && IPAddress.TryParse(Ip, out var addr) ? addr : null;
            set => Ip = value?.ToString();
        }

        /// <summary> The value representing the security enum (as AutoGeneratedDomainNameLabelScope). </summary>
        [EditorBrowsable(EditorBrowsableState.Never)]
        [Obsolete("DnsNameLabelReusePolicy is deprecated, use AutoGeneratedDnsNameLabelScope instead", false)]
        public AutoGeneratedDomainNameLabelScope? DnsNameLabelReusePolicy
        {
            get => AutoGeneratedDomainNameLabelScope.HasValue
                ? new AutoGeneratedDomainNameLabelScope(AutoGeneratedDomainNameLabelScope.Value.ToString())
                : null;
            set => AutoGeneratedDomainNameLabelScope = value.HasValue
                ? new Models.DnsNameLabelReusePolicy(value.Value.ToString())
                : null;
        }
    }
}
