# CreateResponsePayloadValidator — special handling for inline request body
# ---------------------------------------------------------------------------
def generate_create_response_validator(
    spec: dict, overlay: dict, all_schemas: dict, reachable: dict, output_dir: str,
    namespace: str, header: str,
) -> str | None:
    """
    Generate the CreateResponsePayloadValidator from the inline request body schema.
    The request body is inline (anyOf with two identical branches), not a named schema.
    """
    # Find the POST /responses endpoint
    paths = spec.get("paths", {})
    post_schema = None
    for path, methods in paths.items():
        if "post" in methods and "responses" in path.lower():
            post = methods["post"]
            rb = post.get("requestBody", {})
            content = rb.get("content", {}).get("application/json", {})
            schema = content.get("schema", {})
            # anyOf with two identical branches — use the first
            if "anyOf" in schema and len(schema["anyOf"]) > 0:
                post_schema = schema["anyOf"][0]
                break

    if post_schema is None:
        print("WARNING: Could not find POST /responses request body schema", file=sys.stderr)
        return None

    # Apply overlay for CreateResponse
    overlay_entry = overlay.get("schemas", {}).get("CreateResponse", {})
    if "required" in overlay_entry:
        existing = set(post_schema.get("required", []))
        for field in overlay_entry["required"]:
            existing.add(field)
        post_schema["required"] = list(existing)

    # Generate using the standard object validator
    gen = ValidatorGenerator(all_schemas, reachable, namespace=namespace, header=header)
    gen.generated_names = {to_csharp_class(n) for n in reachable}
    gen.generated_names.add("CreateResponsePayloadValidator")

    body_lines = gen._gen_object_body("CreateResponse", post_schema)

    body = "\n".join(f"        {line}" if line else "" for line in body_lines)

    # Compute the parent namespace (strip trailing .Validators if present)
    parent_ns = namespace.rsplit('.Validators', 1)[0] if namespace.endswith('.Validators') else ''
    using_parent = f'\nusing {parent_ns};' if parent_ns else ''

    code = f"""{header}
using System.Text.Json;{using_parent}

namespace {namespace};

/// <summary>
/// Validates a CreateResponse JSON payload against the API schema.
/// Generated from the OpenAPI spec — extend via partial class in Custom/Validators/.
/// </summary>
internal static partial class CreateResponsePayloadValidator
{{
    /// <summary>
    /// Validates a parsed JSON element representing a CreateResponse request body.
    /// </summary>
    public static ValidationResult Validate(JsonElement element)
    {{
{body}
    }}

    /// <summary>
    /// Validates raw UTF-8 JSON bytes representing a CreateResponse request body.
    /// </summary>
    public static ValidationResult Validate(ReadOnlySpan<byte> utf8Json)
    {{
        var reader = new Utf8JsonReader(utf8Json);
        using var doc = JsonDocument.ParseValue(ref reader);
        return Validate(doc.RootElement);
    }}

    /// <summary>
    /// Extension point for custom validation rules.
    /// </summary>
    static partial void ValidateCustom(JsonElement element, List<ValidationError> errors);
}}
"""

    file_path = os.path.join(output_dir, "CreateResponsePayloadValidator.cs")
    with open(file_path, "w", encoding="utf-8") as f:
        f.write(code)
    return file_path


