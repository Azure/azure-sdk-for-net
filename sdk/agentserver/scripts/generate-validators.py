#!/usr/bin/env python3
"""
generate-validators.py — Generate C# validator classes from an OpenAPI 3.0 YAML spec.

Reads the OpenAPI spec, applies an overlay for emitter-missed constraints, walks all
schemas reachable from configurable root schemas and the request body, and emits
C# partial validator classes to the specified output directory.

Usage:
    python3 scripts/generate-validators.py \
        --input path/to/openapi.yaml \
        --output path/to/Generated/Validators \
        --namespace My.Namespace \
        --root-schemas OpenAI.Response \
        --overlay path/to/validation-overlay.yaml  # optional
"""

import argparse
import os
import re
import sys
from pathlib import Path
from typing import Any

import yaml


def make_header(script_path: str = "scripts/generate-validators.py") -> str:
    """Build the auto-generated file header."""
    return f"""// Copyright (c) Microsoft Corporation. All rights reserved.
// Licensed under the MIT License.

#nullable enable
// <auto-generated>
// This file was generated by {script_path} from the OpenAPI spec.
// Do not edit manually. Re-run scripts/Generate-Contracts.ps1 to regenerate.
// </auto-generated>
"""


# ---------------------------------------------------------------------------
# Schema helpers
# ---------------------------------------------------------------------------
def resolve_ref(ref: str) -> str:
    """Extract schema name from a $ref string like '#/components/schemas/OpenAI.Foo'."""
    return ref.rsplit("/", 1)[-1]


def strip_openai_prefix(name: str) -> str:
    """Strip 'OpenAI.' prefix from schema names for C# class naming."""
    if name.startswith("OpenAI."):
        return name[7:]
    return name


def to_csharp_class(name: str) -> str:
    """Convert a schema name to a C# validator class name."""
    clean = strip_openai_prefix(name)
    # Remove hyphens and make PascalCase segments
    parts = clean.replace("-", "_").split("_")
    # Handle already PascalCase names (just ensure first char is upper)
    result = ""
    for part in parts:
        if part:
            result += part[0].upper() + part[1:]
    # Sanitize: remove dots, special chars
    result = re.sub(r"[^A-Za-z0-9]", "", result)
    return result + "Validator"


def is_extensible_enum(schema: dict) -> bool:
    """Returns True if schema uses anyOf [string, string+enum] pattern."""
    if "anyOf" not in schema:
        return False
    branches = schema["anyOf"]
    has_plain_string = any(
        isinstance(b, dict) and b.get("type") == "string" and "enum" not in b
        for b in branches
    )
    has_enum_string = any(
        isinstance(b, dict) and b.get("type") == "string" and "enum" in b
        for b in branches
    )
    return has_plain_string and has_enum_string


def is_closed_enum(schema: dict) -> bool:
    """Returns True if schema is a fixed string enum (type: string + enum: [...])."""
    return schema.get("type") == "string" and "enum" in schema


def get_discriminator(schema: dict) -> dict | None:
    """Get discriminator info if present."""
    return schema.get("discriminator")


def get_properties(schema: dict) -> dict:
    """Get properties dict from a schema, handling allOf/anyOf wrappers."""
    props = schema.get("properties", {})
    # Also merge properties from allOf
    for item in schema.get("allOf", []):
        if isinstance(item, dict) and "properties" in item:
            props.update(item["properties"])
    return props


def get_required_fields(schema: dict) -> list[str]:
    """Get required field names from schema."""
    required = list(schema.get("required", []))
    # Also check allOf for required
    for item in schema.get("allOf", []):
        if isinstance(item, dict) and "required" in item:
            required.extend(item["required"])
    return required


def extract_ref_from_prop(prop_schema: dict) -> str | None:
    """Extract a $ref target from a property schema, handling allOf/anyOf wrappers."""
    if "$ref" in prop_schema:
        return resolve_ref(prop_schema["$ref"])

    # Check allOf: [{$ref: ...}]
    for item in prop_schema.get("allOf", []):
        if isinstance(item, dict) and "$ref" in item:
            return resolve_ref(item["$ref"])

    # Check items for arrays
    items = prop_schema.get("items")
    if isinstance(items, dict) and "$ref" in items:
        return resolve_ref(items["$ref"])

    return None


# ---------------------------------------------------------------------------
# Schema walker — collect all reachable schemas
# ---------------------------------------------------------------------------
class SchemaWalker:
    def __init__(self, schemas: dict[str, dict], overlay: dict):
        self.schemas = schemas
        self.overlay = overlay
        self.visited: set[str] = set()
        self.reachable: dict[str, dict] = {}  # name → schema (with overlay applied)

    def walk(self, name: str) -> None:
        """Recursively walk a schema and all its referenced schemas."""
        if name in self.visited:
            return
        self.visited.add(name)

        schema = self.schemas.get(name)
        if schema is None:
            return

        # Apply overlay
        schema = self._apply_overlay(name, dict(schema))
        self.reachable[name] = schema

        # Walk properties
        props = get_properties(schema)
        for _prop_name, prop_schema in props.items():
            ref = extract_ref_from_prop(prop_schema)
            if ref:
                self.walk(ref)

            # oneOf / anyOf branches
            for branch in prop_schema.get("oneOf", []) + prop_schema.get("anyOf", []):
                if isinstance(branch, dict):
                    if "$ref" in branch:
                        self.walk(resolve_ref(branch["$ref"]))
                    # Walk items.$ref inside inline array branches
                    branch_items = branch.get("items")
                    if isinstance(branch_items, dict) and "$ref" in branch_items:
                        self.walk(resolve_ref(branch_items["$ref"]))

        # Walk discriminator mappings
        disc = get_discriminator(schema)
        if disc and "mapping" in disc:
            for _type_val, ref_str in disc["mapping"].items():
                self.walk(resolve_ref(ref_str))

        # Walk allOf/anyOf/oneOf at schema level
        for combo_key in ("allOf", "anyOf", "oneOf"):
            for item in schema.get(combo_key, []):
                if isinstance(item, dict):
                    if "$ref" in item:
                        self.walk(resolve_ref(item["$ref"]))
                    # Walk items.$ref inside inline array branches
                    inline_items = item.get("items")
                    if isinstance(inline_items, dict) and "$ref" in inline_items:
                        self.walk(resolve_ref(inline_items["$ref"]))

        # Walk items for array schemas
        items = schema.get("items")
        if isinstance(items, dict) and "$ref" in items:
            self.walk(resolve_ref(items["$ref"]))

    def _apply_overlay(self, name: str, schema: dict) -> dict:
        """Apply overlay fixes to a schema."""
        overlay_entry = self.overlay.get("schemas", {}).get(name)
        if not overlay_entry:
            # Also try stripped name
            clean = strip_openai_prefix(name)
            overlay_entry = self.overlay.get("schemas", {}).get(clean)
        if not overlay_entry:
            return schema

        # Merge required fields
        if "required" in overlay_entry:
            existing = set(schema.get("required", []))
            for field in overlay_entry["required"]:
                existing.add(field)
            schema["required"] = list(existing)

        # Remove fields from required (for input schemas that are more relaxed
        # than the merged TypeSpec view, e.g. EasyInputMessage vs ItemMessage)
        # Also mark these properties as nullable since the OpenAI spec/SDK treats
        # them as optional+nullable (e.g. "detail": null is valid when detail is optional).
        if "not_required" in overlay_entry:
            existing = set(schema.get("required", []))
            for field in overlay_entry["not_required"]:
                existing.discard(field)
                # Mark the property as nullable so the validator accepts null values
                if "properties" in schema and field in schema["properties"]:
                    schema["properties"][field]["nullable"] = True
            schema["required"] = list(existing)

        # Merge property-level constraints
        if "properties" in overlay_entry:
            if "properties" not in schema:
                schema["properties"] = {}
            for prop_name, prop_overlay in overlay_entry["properties"].items():
                if prop_name in schema["properties"]:
                    schema["properties"][prop_name].update(prop_overlay)

        return schema


# ---------------------------------------------------------------------------
# C# code generator
# ---------------------------------------------------------------------------
class ValidatorGenerator:
    def __init__(self, all_schemas: dict[str, dict], reachable: dict[str, dict],
                 namespace: str, header: str):
        self.all_schemas = all_schemas
        self.reachable = reachable
        self.namespace = namespace
        self.header = header
        self.generated_names: set[str] = set()  # Track generated class names to avoid duplicates

    def generate_all(self, output_dir: str) -> list[str]:
        """Generate all validator files. Returns list of generated file paths."""
        os.makedirs(output_dir, exist_ok=True)
        generated_files: list[str] = []

        for schema_name, schema in sorted(self.reachable.items()):
            class_name = to_csharp_class(schema_name)

            # Skip duplicates (different OpenAPI names mapping to same C# name)
            if class_name in self.generated_names:
                continue
            self.generated_names.add(class_name)

            code = self._generate_validator(schema_name, schema, class_name)
            if code is None:
                continue

            file_path = os.path.join(output_dir, f"{class_name}.cs")
            with open(file_path, "w", encoding="utf-8") as f:
                f.write(code)
            generated_files.append(file_path)

        return generated_files

    def _generate_validator(self, schema_name: str, schema: dict, class_name: str) -> str | None:
        """Generate a complete C# validator file for a schema."""
        disc = get_discriminator(schema)

        # Skip schemas that are just enum definitions (closed or extensible) with no properties
        if is_closed_enum(schema) or is_extensible_enum(schema):
            # Enums are validated inline at the property level, not as separate validators
            return None

        # Skip schemas that are just type aliases or too simple
        if schema.get("type") in ("string", "integer", "number", "boolean") and "properties" not in schema:
            return None

        # Array schema — no standalone validator needed (validated at property level)
        if schema.get("type") == "array" and "properties" not in schema:
            return None

        body_lines: list[str] = []

        if disc:
            # Discriminator dispatch validator
            self._pending_dispatch_method = None
            body_lines = self._gen_discriminator_body(schema_name, schema, disc)
        else:
            # Regular object validator
            self._pending_dispatch_method = None
            body_lines = self._gen_object_body(schema_name, schema)

        if not body_lines:
            return None

        return self._wrap_class(class_name, body_lines)

    def _wrap_class(self, class_name: str, body_lines: list[str]) -> str:
        """Wrap validation logic in a C# class."""
        body = "\n".join(f"        {line}" if line else "" for line in body_lines)

        # Build extra members for discriminator validators
        extra_members = ""
        if self._pending_dispatch_method:
            dispatch_body = "\n".join(
                f"    {line}" if line else "" for line in self._pending_dispatch_method
            )
            extra_members = f"""

    /// <summary>
    /// Extension point: return a default discriminator value when the property is missing.
    /// Override in a partial class to supply a default (e.g. return "message").
    /// Returns null by default, which means the discriminator is required.
    /// </summary>
    static partial void ResolveDefaultDiscriminator(JsonElement element, ref string? defaultType);

    private static string? GetDefaultDiscriminator(JsonElement element)
    {{
        string? defaultType = null;
        ResolveDefaultDiscriminator(element, ref defaultType);
        return defaultType;
    }}

{dispatch_body}"""
            self._pending_dispatch_method = None

        # Compute the parent namespace (strip trailing .Validators if present)
        parent_ns = self.namespace.rsplit('.Validators', 1)[0] if self.namespace.endswith('.Validators') else ''
        using_parent = f'\nusing {parent_ns};' if parent_ns else ''

        return f"""{self.header}
using System.Text.Json;{using_parent}

namespace {self.namespace};

/// <summary>
/// Validates JSON payloads against the API schema.
/// Extend via partial class in Custom/Validators/ to add custom rules.
/// </summary>
internal static partial class {class_name}
{{
    /// <summary>
    /// Validates a parsed JSON element.
    /// </summary>
    public static ValidationResult Validate(JsonElement element)
    {{
{body}
    }}

    /// <summary>
    /// Extension point for custom validation rules.
    /// </summary>
    static partial void ValidateCustom(JsonElement element, List<ValidationError> errors);{extra_members}
}}
"""

    def _gen_discriminator_body(self, schema_name: str, schema: dict, disc: dict) -> list[str]:
        """Generate validation body for a discriminated union."""
        prop_name = disc.get("propertyName", "type")
        mapping = disc.get("mapping", {})
        lines: list[str] = []

        # Guard: element must be an object before accessing properties
        lines.append("if (element.ValueKind != JsonValueKind.Object)")
        lines.append("{")
        lines.append(f'    return ValidationResult.Failure([new ValidationError("$", '
                      f'$"Expected object, got {{element.ValueKind}}")]);')
        lines.append("}")
        lines.append("")
        lines.append("var errors = new List<ValidationError>();")
        lines.append("")
        lines.append("// Discriminator dispatch")
        lines.append(f'string resolvedType;')
        lines.append(f'if (!element.TryGetProperty("{prop_name}", out var typeProp) ||')
        lines.append(f"    typeProp.ValueKind != JsonValueKind.String)")
        lines.append("{")
        lines.append(f'    var defaultType = GetDefaultDiscriminator(element);')
        lines.append(f'    if (defaultType is null)')
        lines.append(f"        return ValidationResult.Failure([new ValidationError(\"$.{prop_name}\", "
                      f"\"Required discriminator '{prop_name}' is missing or not a string\")]);")
        lines.append(f'    resolvedType = defaultType;')
        lines.append("}")
        lines.append("else")
        lines.append("{")
        lines.append(f'    resolvedType = typeProp.GetString()!;')
        lines.append("}")
        lines.append("")
        lines.append("var dispatchResult = DispatchValidation(resolvedType, element);")
        lines.append("if (!dispatchResult.IsValid)")
        lines.append("{")
        lines.append("    foreach (var e in dispatchResult.Errors)")
        lines.append("        errors.Add(e);")
        lines.append("}")
        lines.append("")
        lines.append("ValidateCustom(element, errors);")
        lines.append("")
        lines.append("return errors.Count == 0 ? ValidationResult.Success : ValidationResult.Failure(errors);")

        # Generate the DispatchValidation method lines (stored separately)
        self._pending_dispatch_method = []
        self._pending_dispatch_method.append("")
        self._pending_dispatch_method.append("private static ValidationResult DispatchValidation(string typeValue, JsonElement element) => typeValue switch")
        self._pending_dispatch_method.append("{")

        for type_val, ref_str in sorted(mapping.items()):
            ref_name = resolve_ref(ref_str)
            variant_class = to_csharp_class(ref_name)
            if variant_class in self.generated_names or ref_name in self.reachable:
                self._pending_dispatch_method.append(f'    "{type_val}" => {variant_class}.Validate(element),')
            else:
                self._pending_dispatch_method.append(f'    "{type_val}" => ValidationResult.Success,')

        self._pending_dispatch_method.append('    _ => ValidationResult.Success,  // Unknown type — forward compatible')
        self._pending_dispatch_method.append("};")

        return lines

    # ------------------------------------------------------------------
    # Union analysis helpers
    # ------------------------------------------------------------------
    def _get_union_branches(self, branches: list[dict]) -> list[tuple[str, str, dict]]:
        """Analyze oneOf/anyOf branches and return (accepted_kind, validation_mode, details) tuples.

        accepted_kind:  JsonValueKind name (e.g. "String", "Number", "Object", "Array")
        validation_mode: "simple" | "enum" | "delegate" | "any"
        details:        extra info (enum values, validator class name, etc.)
        """
        result: list[tuple[str, str, dict]] = []
        for branch in branches:
            if not isinstance(branch, dict):
                continue

            # Resolve $ref
            if "$ref" in branch:
                ref_name = resolve_ref(branch["$ref"])
                ref_schema = self.all_schemas.get(ref_name, {})

                if is_closed_enum(ref_schema):
                    result.append(("String", "enum", {"values": ref_schema.get("enum", [])}))
                elif is_extensible_enum(ref_schema):
                    result.append(("String", "simple", {}))
                elif get_discriminator(ref_schema) or ref_schema.get("type") == "object" or "properties" in ref_schema:
                    class_name = to_csharp_class(ref_name)
                    result.append(("Object", "delegate", {"class": class_name, "ref": ref_name}))
                elif "oneOf" in ref_schema or "anyOf" in ref_schema:
                    # Recursive union — unwrap
                    inner_branches = ref_schema.get("oneOf", ref_schema.get("anyOf", []))
                    result.extend(self._get_union_branches(inner_branches))
                elif ref_schema.get("type") == "array":
                    ref_items = ref_schema.get("items", {})
                    result.append(("Array", "array", {"items": ref_items}))
                elif ref_schema.get("type") == "string":
                    result.append(("String", "simple", {}))
                elif ref_schema.get("type") == "number" or ref_schema.get("type") == "integer":
                    result.append(("Number", "simple", {}))
                else:
                    result.append(("Object", "delegate", {"class": to_csharp_class(ref_name), "ref": ref_name}))
                continue

            # Inline branch
            branch_type = branch.get("type")
            if branch_type == "string":
                if "enum" in branch:
                    result.append(("String", "enum", {"values": branch["enum"]}))
                else:
                    result.append(("String", "simple", {}))
            elif branch_type == "number" or branch_type == "integer":
                result.append(("Number", "simple", {}))
            elif branch_type == "boolean":
                result.append(("True", "simple", {}))
                result.append(("False", "simple", {}))
            elif branch_type == "array":
                branch_items = branch.get("items", {})
                result.append(("Array", "array", {"items": branch_items}))
            elif branch_type == "object":
                if "additionalProperties" in branch and not branch.get("properties"):
                    result.append(("Object", "simple", {}))
                else:
                    result.append(("Object", "simple", {}))
            elif not branch_type and not branch.get("$ref"):
                # Schemaless branch (e.g. `{}`) — accept anything
                result.append(("", "any", {}))

        return result

    def _gen_union_check(self, var: str, json_path: str, branches: list[dict]) -> list[str]:
        """Generate C# value-kind validation code from union branch analysis."""
        analyzed = self._get_union_branches(branches)
        if not analyzed:
            return []

        # Check for "any" mode (schemaless branch) — accept anything
        if any(mode == "any" for _, mode, _ in analyzed):
            return []

        # Collect accepted kinds (deduplicate)
        accepted_kinds: list[str] = []
        for kind, _mode, _details in analyzed:
            if kind and kind not in accepted_kinds:
                accepted_kinds.append(kind)

        if not accepted_kinds:
            return []

        lines: list[str] = []

        # Build the kind check condition
        kind_checks = []
        for kind in accepted_kinds:
            if kind in ("True", "False"):
                # Boolean needs both True and False
                if "True" not in [k for k in kind_checks]:
                    kind_checks.append(f"{var}.ValueKind != JsonValueKind.True")
                if "False" not in [k for k in kind_checks]:
                    kind_checks.append(f"{var}.ValueKind != JsonValueKind.False")
            else:
                kind_checks.append(f"{var}.ValueKind != JsonValueKind.{kind}")

        if len(accepted_kinds) == 1 and accepted_kinds[0] not in ("True", "False"):
            # Single type — simple check
            kind_name = accepted_kinds[0].lower()
            lines.append(f"if ({kind_checks[0]})")
            lines.append(f'    errors.Add(new ValidationError("{json_path}", '
                          f'$"Expected {kind_name}, got {{{var}.ValueKind}}"));')
        else:
            # Multiple types — combined check
            kind_names = []
            for k in accepted_kinds:
                if k in ("True", "False"):
                    if "boolean" not in kind_names:
                        kind_names.append("boolean")
                else:
                    kind_names.append(k.lower())
            expected_str = " or ".join(kind_names)

            # All checks must fail for this to be an error
            condition_parts = []
            seen_bool = False
            for kind in accepted_kinds:
                if kind in ("True", "False"):
                    if not seen_bool:
                        condition_parts.append(
                            f"{var}.ValueKind != JsonValueKind.True && "
                            f"{var}.ValueKind != JsonValueKind.False")
                        seen_bool = True
                else:
                    condition_parts.append(f"{var}.ValueKind != JsonValueKind.{kind}")

            condition = " && ".join(condition_parts)
            lines.append(f"if ({condition})")
            lines.append(f'    errors.Add(new ValidationError("{json_path}", '
                          f'$"Expected {expected_str}, got {{{var}.ValueKind}}"));')

        # For delegates, add sub-validation when kind matches
        delegates = [(kind, details) for kind, mode, details in analyzed if mode == "delegate"]
        if delegates and len(delegates) == 1:
            kind, details = delegates[0]
            ref_class = details["class"]
            ref_name = details.get("ref", "")
            if ref_name in self.reachable or ref_class in self.generated_names:
                # Build a safe variable name from json_path
                clean_path = json_path.replace("$.", "").replace("$", "root").replace(".", "_")
                safe_var = self._var_name(clean_path) if clean_path else "rootVal"
                lines.append(f"else if ({var}.ValueKind == JsonValueKind.{kind})")
                lines.append("{")
                lines.append(f"    var {safe_var}Result = {ref_class}.Validate({var});")
                lines.append(f"    if (!{safe_var}Result.IsValid)")
                lines.append("    {")
                lines.append(f"        foreach (var e in {safe_var}Result.Errors)")
                lines.append(f'            errors.Add(new ValidationError("{json_path}" + e.Path.Substring(1), e.Message));')
                lines.append("    }")
                lines.append("}")

        # For array branches with item refs, add per-item validation
        array_branches = [(details) for kind, mode, details in analyzed if mode == "array"]
        for arr_details in array_branches:
            arr_items = arr_details.get("items", {})
            item_ref = None
            if "$ref" in arr_items:
                item_ref = resolve_ref(arr_items["$ref"])
            if item_ref and item_ref in self.reachable:
                item_class = to_csharp_class(item_ref)
                clean_path = json_path.replace("$.", "").replace("$", "root").replace(".", "_")
                idx_var = self._var_name(clean_path + "Idx") if clean_path else "rootIdx"
                lines.append(f"else if ({var}.ValueKind == JsonValueKind.Array)")
                lines.append("{")
                lines.append(f"    var {idx_var} = 0;")
                lines.append(f"    foreach (var item in {var}.EnumerateArray())")
                lines.append("    {")
                lines.append(f"        var itemResult = {item_class}.Validate(item);")
                lines.append("        if (!itemResult.IsValid)")
                lines.append("        {")
                lines.append("            foreach (var e in itemResult.Errors)")
                lines.append(f'                errors.Add(new ValidationError($"{json_path}[{{{idx_var}}}]" + e.Path.Substring(1), e.Message));')
                lines.append("        }")
                lines.append(f"        {idx_var}++;")
                lines.append("    }")
                lines.append("}")
                break  # Only one array branch per union

        return lines

    # ------------------------------------------------------------------
    # Body generators
    # ------------------------------------------------------------------
    def _gen_union_body(self, schema_name: str, schema: dict) -> list[str]:
        """Generate validation body for a standalone union schema (oneOf/anyOf, no properties)."""
        branches = schema.get("oneOf", schema.get("anyOf", []))
        if not branches:
            return ["return ValidationResult.Success;"]

        lines: list[str] = []
        lines.append("var errors = new List<ValidationError>();")
        lines.append("")
        union_lines = self._gen_union_check("element", "$", branches)
        if union_lines:
            lines.extend(union_lines)
            lines.append("")
        lines.append("ValidateCustom(element, errors);")
        lines.append("")
        lines.append("return errors.Count == 0 ? ValidationResult.Success : ValidationResult.Failure(errors);")
        return lines

    def _gen_map_body(self, schema_name: str, schema: dict) -> list[str]:
        """Generate validation body for a map schema (additionalProperties)."""
        addl = schema.get("additionalProperties", {})
        lines: list[str] = []

        lines.append("var errors = new List<ValidationError>();")
        lines.append("")
        lines.append("if (element.ValueKind != JsonValueKind.Object)")
        lines.append("{")
        lines.append('    errors.Add(new ValidationError("$", '
                      '$"Expected object, got {element.ValueKind}"));')
        lines.append("    return ValidationResult.Failure(errors);")
        lines.append("}")
        lines.append("")

        # Value-type validation
        if isinstance(addl, dict) and addl:
            value_type = addl.get("type")
            if value_type:
                expected_kind = self._type_to_json_kind(value_type)
                if expected_kind:
                    kind_name = value_type
                    lines.append("foreach (var kvp in element.EnumerateObject())")
                    lines.append("{")
                    if value_type == "boolean":
                        lines.append("    if (kvp.Value.ValueKind != JsonValueKind.True && "
                                      "kvp.Value.ValueKind != JsonValueKind.False)")
                    else:
                        lines.append(f"    if (kvp.Value.ValueKind != JsonValueKind.{expected_kind} && "
                                      "kvp.Value.ValueKind != JsonValueKind.Null)")
                    lines.append(f'        errors.Add(new ValidationError($"$.{{kvp.Name}}", '
                                  f'$"Expected {kind_name}, got {{kvp.Value.ValueKind}}"));')
                    lines.append("}")
                    lines.append("")
            elif "anyOf" in addl:
                # Union value types (e.g., string|number|boolean)
                branch_checks = self._get_union_branches(addl["anyOf"])
                if branch_checks:
                    accepted_kinds: list[str] = []
                    for kind, _mode, _details in branch_checks:
                        if kind and kind not in accepted_kinds:
                            accepted_kinds.append(kind)
                    if accepted_kinds:
                        kind_names = []
                        conditions = []
                        seen_bool = False
                        for kind in accepted_kinds:
                            if kind in ("True", "False"):
                                if not seen_bool:
                                    kind_names.append("boolean")
                                    conditions.append("kvp.Value.ValueKind != JsonValueKind.True && "
                                                      "kvp.Value.ValueKind != JsonValueKind.False")
                                    seen_bool = True
                            else:
                                kind_names.append(kind.lower())
                                conditions.append(f"kvp.Value.ValueKind != JsonValueKind.{kind}")
                        expected_str = " or ".join(kind_names)
                        condition = " && ".join(conditions)
                        lines.append("foreach (var kvp in element.EnumerateObject())")
                        lines.append("{")
                        lines.append(f"    if ({condition} && kvp.Value.ValueKind != JsonValueKind.Null)")
                        lines.append(f'        errors.Add(new ValidationError($"$.{{kvp.Name}}", '
                                      f'$"Expected {expected_str}, got {{kvp.Value.ValueKind}}"));')
                        lines.append("}")
                        lines.append("")

        lines.append("ValidateCustom(element, errors);")
        lines.append("")
        lines.append("return errors.Count == 0 ? ValidationResult.Success : ValidationResult.Failure(errors);")
        return lines

    def _gen_object_body(self, schema_name: str, schema: dict) -> list[str]:
        """Generate validation body for a regular object schema."""
        props = get_properties(schema)
        required = set(get_required_fields(schema))
        lines: list[str] = []

        if not props and not required:
            # Check for union type (oneOf/anyOf) before falling back to no-op
            if "oneOf" in schema or "anyOf" in schema:
                return self._gen_union_body(schema_name, schema)
            # Check for map type (additionalProperties)
            if "additionalProperties" in schema:
                return self._gen_map_body(schema_name, schema)
            # Opaque object — at minimum check value kind
            if schema.get("type") == "object":
                return [
                    "if (element.ValueKind != JsonValueKind.Object)",
                    '    return ValidationResult.Failure([new ValidationError("$", '
                    '$"Expected object, got {element.ValueKind}")]);',
                    "",
                    "return ValidationResult.Success;",
                ]
            # Nothing to validate
            return ["return ValidationResult.Success;"]

        lines.append("var errors = new List<ValidationError>();")
        lines.append("")

        # Check it's an object
        lines.append("if (element.ValueKind != JsonValueKind.Object)")
        lines.append("{")
        lines.append('    errors.Add(new ValidationError("$", '
                      '$"Expected object, got {element.ValueKind}"));')
        lines.append("    return ValidationResult.Failure(errors);")
        lines.append("}")
        lines.append("")

        # Validate each property
        for prop_name, prop_schema in sorted(props.items()):
            is_required = prop_name in required
            prop_lines = self._gen_property_validation(prop_name, prop_schema, is_required)
            lines.extend(prop_lines)
            if prop_lines:
                lines.append("")

        # Custom validation hook
        lines.append("ValidateCustom(element, errors);")
        lines.append("")
        lines.append("return errors.Count == 0 ? ValidationResult.Success : ValidationResult.Failure(errors);")

        return lines

    def _gen_property_validation(self, prop_name: str, prop_schema: dict, is_required: bool) -> list[str]:
        """Generate validation lines for a single property."""
        lines: list[str] = []
        nullable = prop_schema.get("nullable", False)
        json_path = f"$.{prop_name}"

        # Resolve through allOf wrappers to find the actual type
        effective_schema = prop_schema
        if "allOf" in prop_schema and len(prop_schema.get("allOf", [])) > 0:
            for item in prop_schema["allOf"]:
                if isinstance(item, dict):
                    if "$ref" in item:
                        ref_name = resolve_ref(item["$ref"])
                        if ref_name in self.all_schemas:
                            effective_schema = self.all_schemas[ref_name]
                    elif "properties" in item or "type" in item:
                        effective_schema = item

        prop_type = effective_schema.get("type", prop_schema.get("type"))
        ref_target = extract_ref_from_prop(prop_schema)

        if is_required and not nullable:
            lines.append(f"// Required: {prop_name}")
            lines.append(f'if (!element.TryGetProperty("{prop_name}", out var {self._var_name(prop_name)}Prop))')
            lines.append(f"    errors.Add(new ValidationError(\"{json_path}\", "
                          f"\"Required property '{prop_name}' is missing\"));")
            lines.append("else")
            lines.append("{")
            inner = self._gen_type_check(prop_name, prop_schema, effective_schema, json_path, nullable)
            for il in inner:
                lines.append(f"    {il}")
            lines.append("}")
        elif is_required and nullable:
            lines.append(f"// Required (nullable): {prop_name}")
            lines.append(f'if (!element.TryGetProperty("{prop_name}", out var {self._var_name(prop_name)}Prop))')
            lines.append(f"    errors.Add(new ValidationError(\"{json_path}\", "
                          f"\"Required property '{prop_name}' is missing\"));")
            lines.append(f"else if ({self._var_name(prop_name)}Prop.ValueKind != JsonValueKind.Null)")
            lines.append("{")
            inner = self._gen_type_check(prop_name, prop_schema, effective_schema, json_path, nullable=True)
            for il in inner:
                lines.append(f"    {il}")
            lines.append("}")
        else:
            # Optional property — only validate if present
            lines.append(f"// Optional: {prop_name}")
            lines.append(f'if (element.TryGetProperty("{prop_name}", out var {self._var_name(prop_name)}Prop)'
                          f'{" && " + self._var_name(prop_name) + "Prop.ValueKind != JsonValueKind.Null" if nullable else ""})')
            lines.append("{")
            inner = self._gen_type_check(prop_name, prop_schema, effective_schema, json_path, nullable)
            for il in inner:
                lines.append(f"    {il}")
            lines.append("}")

        return lines

    def _gen_type_check(self, prop_name: str, prop_schema: dict, effective_schema: dict,
                        json_path: str, nullable: bool) -> list[str]:
        """Generate type-checking lines for a property value."""
        lines: list[str] = []
        var = f"{self._var_name(prop_name)}Prop"
        prop_type = effective_schema.get("type", prop_schema.get("type"))
        ref_target = extract_ref_from_prop(prop_schema)

        # Handle oneOf (union types) — both inline and with $ref branches
        if "oneOf" in prop_schema:
            return self._gen_union_check(var, json_path, prop_schema["oneOf"])

        # Handle anyOf (union types) — inline branches
        if "anyOf" in prop_schema and not is_extensible_enum(prop_schema):
            return self._gen_union_check(var, json_path, prop_schema["anyOf"])

        # Handle arrays with item refs BEFORE ref-based delegation.
        # extract_ref_from_prop() extracts items.$ref for array properties, but
        # the ref-based code below would treat that as a direct object reference.
        # Arrays must be handled first so elements are iterated, not passed whole.
        if prop_type == "array" or prop_schema.get("type") == "array":
            items = prop_schema.get("items") or effective_schema.get("items")
            if items:
                return self._gen_array_check(prop_name, var, json_path, items)
            else:
                lines.append(f"if ({var}.ValueKind != JsonValueKind.Array)")
                lines.append(f'    errors.Add(new ValidationError("{json_path}", '
                              f'$"Expected array, got {{{var}.ValueKind}}"));')
                return lines

        # Handle referenced schemas
        if ref_target and ref_target in self.reachable:
            ref_schema = self.reachable[ref_target]
            ref_class = to_csharp_class(ref_target)

            if is_closed_enum(ref_schema):
                enum_values = ref_schema.get("enum", [])
                return self._gen_closed_enum_check(var, json_path, enum_values)
            elif is_extensible_enum(ref_schema):
                return self._gen_extensible_enum_check(var, json_path)
            elif ref_schema.get("type") == "array":
                # Referenced array schema — validate inline as array
                items = ref_schema.get("items")
                if items:
                    return self._gen_array_check(prop_name, var, json_path, items)
                else:
                    lines.append(f"if ({var}.ValueKind != JsonValueKind.Array)")
                    lines.append(f'    errors.Add(new ValidationError("{json_path}", '
                                  f'$"Expected array, got {{{var}.ValueKind}}"));')
                    return lines
            elif get_discriminator(ref_schema) or ref_schema.get("type") == "object" or "properties" in ref_schema:
                lines.append(f"var {self._var_name(prop_name)}Result = {ref_class}.Validate({var});")
                lines.append(f"if (!{self._var_name(prop_name)}Result.IsValid)")
                lines.append("{")
                lines.append(f"    foreach (var e in {self._var_name(prop_name)}Result.Errors)")
                lines.append(f'        errors.Add(new ValidationError("{json_path}" + e.Path.Substring(1), e.Message));')
                lines.append("}")
                return lines
            elif "oneOf" in ref_schema or "anyOf" in ref_schema:
                # Pattern D fix: ref target is a union type — delegate to its validator
                lines.append(f"var {self._var_name(prop_name)}Result = {ref_class}.Validate({var});")
                lines.append(f"if (!{self._var_name(prop_name)}Result.IsValid)")
                lines.append("{")
                lines.append(f"    foreach (var e in {self._var_name(prop_name)}Result.Errors)")
                lines.append(f'        errors.Add(new ValidationError("{json_path}" + e.Path.Substring(1), e.Message));')
                lines.append("}")
                return lines
            elif "additionalProperties" in ref_schema:
                # Pattern D fix: ref target is a map type — delegate to its validator
                lines.append(f"var {self._var_name(prop_name)}Result = {ref_class}.Validate({var});")
                lines.append(f"if (!{self._var_name(prop_name)}Result.IsValid)")
                lines.append("{")
                lines.append(f"    foreach (var e in {self._var_name(prop_name)}Result.Errors)")
                lines.append(f'        errors.Add(new ValidationError("{json_path}" + e.Path.Substring(1), e.Message));')
                lines.append("}")
                return lines

        # Handle referenced schemas not in reachable but in all_schemas (e.g., array wrappers)
        if ref_target and ref_target not in self.reachable and ref_target in self.all_schemas:
            ref_schema = self.all_schemas[ref_target]
            if ref_schema.get("type") == "array":
                items = ref_schema.get("items")
                if items:
                    return self._gen_array_check(prop_name, var, json_path, items)
            elif "oneOf" in ref_schema or "anyOf" in ref_schema:
                # Union type not in reachable — validate inline
                branches = ref_schema.get("oneOf", ref_schema.get("anyOf", []))
                return self._gen_union_check(var, json_path, branches)

        # Handle effective_schema union types (from allOf-wrapped refs)
        if effective_schema is not prop_schema:
            if "oneOf" in effective_schema and not is_closed_enum(effective_schema):
                return self._gen_union_check(var, json_path, effective_schema["oneOf"])
            if "anyOf" in effective_schema and not is_extensible_enum(effective_schema):
                return self._gen_union_check(var, json_path, effective_schema["anyOf"])

        # Handle inline closed enums
        if is_closed_enum(effective_schema):
            enum_values = effective_schema.get("enum", [])
            return self._gen_closed_enum_check(var, json_path, enum_values)

        # Handle inline extensible enums
        if is_extensible_enum(effective_schema) or is_extensible_enum(prop_schema):
            return self._gen_extensible_enum_check(var, json_path)

        # Simple type checks
        expected_kind = self._type_to_json_kind(prop_type)
        if expected_kind:
            if prop_type == "number":
                # numbers can be Number kind
                lines.append(f"if ({var}.ValueKind != JsonValueKind.Number)")
                lines.append(f'    errors.Add(new ValidationError("{json_path}", '
                              f'$"Expected number, got {{{var}.ValueKind}}"));')
                # Range checks
                range_lines = self._gen_range_check(var, json_path, effective_schema, prop_schema)
                lines.extend(range_lines)
            elif prop_type == "integer":
                lines.append(f"if ({var}.ValueKind != JsonValueKind.Number)")
                lines.append(f'    errors.Add(new ValidationError("{json_path}", '
                              f'$"Expected integer, got {{{var}.ValueKind}}"));')
                range_lines = self._gen_range_check(var, json_path, effective_schema, prop_schema)
                lines.extend(range_lines)
            elif prop_type == "string":
                lines.append(f"if ({var}.ValueKind != JsonValueKind.String)")
                lines.append(f'    errors.Add(new ValidationError("{json_path}", '
                              f'$"Expected string, got {{{var}.ValueKind}}"));')
                # String length checks
                range_lines = self._gen_string_length_check(var, json_path, effective_schema, prop_schema)
                lines.extend(range_lines)
            elif prop_type == "boolean":
                lines.append(f"if ({var}.ValueKind != JsonValueKind.True && "
                              f"{var}.ValueKind != JsonValueKind.False)")
                lines.append(f'    errors.Add(new ValidationError("{json_path}", '
                              f'$"Expected boolean, got {{{var}.ValueKind}}"));')
            elif prop_type == "object":
                lines.append(f"if ({var}.ValueKind != JsonValueKind.Object)")
                lines.append(f'    errors.Add(new ValidationError("{json_path}", '
                              f'$"Expected object, got {{{var}.ValueKind}}"));')
                # If there's a ref target with properties, delegate
                if ref_target and ref_target in self.reachable:
                    ref_class = to_csharp_class(ref_target)
                    lines.append("else")
                    lines.append("{")
                    lines.append(f"    var {self._var_name(prop_name)}Result = {ref_class}.Validate({var});")
                    lines.append(f"    if (!{self._var_name(prop_name)}Result.IsValid)")
                    lines.append("    {")
                    lines.append(f"        foreach (var e in {self._var_name(prop_name)}Result.Errors)")
                    lines.append(f'            errors.Add(new ValidationError("{json_path}" + e.Path.Substring(1), e.Message));')
                    lines.append("    }")
                    lines.append("}")

        return lines

    def _gen_closed_enum_check(self, var: str, json_path: str, enum_values: list) -> list[str]:
        """Generate check for a closed enum."""
        lines: list[str] = []
        lines.append(f"if ({var}.ValueKind != JsonValueKind.String)")
        lines.append(f'    errors.Add(new ValidationError("{json_path}", '
                      f'$"Expected string, got {{{var}.ValueKind}}"));')

        if len(enum_values) <= 8:
            values_check = " or ".join(f'"{v}"' for v in enum_values)
            values_list = ", ".join(str(v) for v in enum_values)
            lines.append(f"else if ({var}.GetString() is not ({values_check}))")
            lines.append(f"    errors.Add(new ValidationError(\"{json_path}\", "
                          f"$\"Value '{{{var}.GetString()}}' is not valid. "
                          f"Allowed: {values_list}\"));")
        else:
            # Use a HashSet for large enums
            set_name = f"s_{self._var_name(json_path.replace('$.', '').replace('.', '_'))}Values"
            lines.append(f"else if (!{var}.GetString()!.Equals({var}.GetString(), StringComparison.Ordinal))")
            lines.append("{")
            lines.append(f"    // Large enum — see AllowedValues check below")
            lines.append("}")
            # Actually, let's just do a contains check with all values
            values_list = ", ".join(str(v) for v in enum_values[:8])
            remaining = len(enum_values) - 8
            lines = []  # Reset
            lines.append(f"if ({var}.ValueKind != JsonValueKind.String)")
            lines.append(f'    errors.Add(new ValidationError("{json_path}", '
                          f'$"Expected string, got {{{var}.ValueKind}}"));')

        return lines

    def _gen_extensible_enum_check(self, var: str, json_path: str) -> list[str]:
        """Generate check for an extensible enum — only validate type is string."""
        return [
            f"if ({var}.ValueKind != JsonValueKind.String)",
            f'    errors.Add(new ValidationError("{json_path}", '
            f'$"Expected string, got {{{var}.ValueKind}}"));',
        ]

    def _gen_array_check(self, prop_name: str, var: str, json_path: str, items: dict) -> list[str]:
        """Generate validation for array properties with item validation."""
        lines: list[str] = []
        lines.append(f"if ({var}.ValueKind != JsonValueKind.Array)")
        lines.append(f'    errors.Add(new ValidationError("{json_path}", '
                      f'$"Expected array, got {{{var}.ValueKind}}"));')

        item_ref = None
        if "$ref" in items:
            item_ref = resolve_ref(items["$ref"])

        # Skip delegation for primitive schemas and extensible enums (integer,
        # number, string, boolean, anyOf[string, string-with-enum]) — these are
        # validated inline, not via separate validator classes.
        if item_ref and item_ref in self.reachable:
            ref_schema = self.all_schemas.get(item_ref, {})
            is_primitive = (
                ref_schema.get("type") in ("string", "integer", "number", "boolean")
                and "properties" not in ref_schema
                and "oneOf" not in ref_schema
                and "anyOf" not in ref_schema
            )
            is_ext_enum = is_extensible_enum(ref_schema)
            if not is_primitive and not is_ext_enum:
                item_class = to_csharp_class(item_ref)
                idx_var = f"{self._var_name(prop_name)}Idx"
                lines.append("else")
                lines.append("{")
                lines.append(f"    var {idx_var} = 0;")
                lines.append(f"    foreach (var item in {var}.EnumerateArray())")
                lines.append("    {")
                lines.append(f"        var itemResult = {item_class}.Validate(item);")
                lines.append("        if (!itemResult.IsValid)")
                lines.append("        {")
                lines.append("            foreach (var e in itemResult.Errors)")
                lines.append(f'                errors.Add(new ValidationError($"{json_path}[{{{idx_var}}}]" + e.Path.Substring(1), e.Message));')
                lines.append("        }")
                lines.append(f"        {idx_var}++;")
                lines.append("    }")
                lines.append("}")

        return lines

    def _gen_range_check(self, var: str, json_path: str,
                         effective_schema: dict, prop_schema: dict) -> list[str]:
        """Generate numeric range checks (minimum/maximum)."""
        lines: list[str] = []
        minimum = effective_schema.get("minimum", prop_schema.get("minimum"))
        maximum = effective_schema.get("maximum", prop_schema.get("maximum"))

        if minimum is not None or maximum is not None:
            lines.append("else")
            lines.append("{")
            lines.append(f"    var val = {var}.GetDouble();")
            if minimum is not None and maximum is not None:
                lines.append(f"    if (val < {minimum} || val > {maximum})")
                lines.append(f'        errors.Add(new ValidationError("{json_path}", '
                              f'$"Value {{val}} is outside allowed range [{minimum}, {maximum}]"));')
            elif minimum is not None:
                lines.append(f"    if (val < {minimum})")
                lines.append(f'        errors.Add(new ValidationError("{json_path}", '
                              f'$"Value {{val}} must be >= {minimum}"));')
            elif maximum is not None:
                lines.append(f"    if (val > {maximum})")
                lines.append(f'        errors.Add(new ValidationError("{json_path}", '
                              f'$"Value {{val}} must be <= {maximum}"));')
            lines.append("}")

        return lines

    def _gen_string_length_check(self, var: str, json_path: str,
                                  effective_schema: dict, prop_schema: dict) -> list[str]:
        """Generate string length checks (minLength/maxLength)."""
        lines: list[str] = []
        min_len = effective_schema.get("minLength", prop_schema.get("minLength"))
        max_len = effective_schema.get("maxLength", prop_schema.get("maxLength"))

        if min_len is not None or max_len is not None:
            lines.append("else")
            lines.append("{")
            lines.append(f"    var strVal = {var}.GetString()!;")
            if min_len is not None:
                lines.append(f"    if (strVal.Length < {min_len})")
                lines.append(f'        errors.Add(new ValidationError("{json_path}", '
                              f'$"String length {{strVal.Length}} is below minimum {min_len}"));')
            if max_len is not None:
                lines.append(f"    if (strVal.Length > {max_len})")
                lines.append(f'        errors.Add(new ValidationError("{json_path}", '
                              f'$"String length {{strVal.Length}} exceeds maximum {max_len}"));')
            lines.append("}")

        return lines

    def _type_to_json_kind(self, type_name: str | None) -> str | None:
        """Map OpenAPI type to JsonValueKind."""
        return {
            "string": "String",
            "integer": "Number",
            "number": "Number",
            "boolean": "True",
            "object": "Object",
            "array": "Array",
        }.get(type_name or "")

    def _var_name(self, name: str) -> str:
        """Convert a property name to a safe C# variable name."""
        # camelCase, remove special chars
        parts = name.replace("-", "_").replace(".", "_").split("_")
        result = parts[0].lower()
        for p in parts[1:]:
            if p:
                result += p[0].upper() + p[1:]
        # Ensure not a C# keyword
        if result in ("class", "object", "event", "string", "int", "bool", "item",
                       "type", "error", "default", "base", "new", "null", "true", "false"):
            result += "Val"
        return result


