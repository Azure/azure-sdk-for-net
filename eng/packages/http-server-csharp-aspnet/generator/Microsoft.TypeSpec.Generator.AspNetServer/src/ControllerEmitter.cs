using System;
using System.Collections.Generic;
using System.IO;
using System.Linq;
using System.Text;
using System.Threading.Tasks;
using Microsoft.TypeSpec.Generator.Input;
using Microsoft.TypeSpec.Generator.Primitives;

namespace Microsoft.TypeSpec.Generator.AspNetServer
{
    /// <summary>
    /// Emits one ASP.NET Core controller base file per <see cref="InputClient"/>.
    /// Controllers reference <c>Microsoft.AspNetCore.Mvc</c> types which are not
    /// loaded in the generator's runtime, so we emit raw C# source here instead
    /// of going through the structured TypeProvider pipeline (which requires
    /// loadable <see cref="System.Type"/> for every reference).
    /// </summary>
    internal static class ControllerEmitter
    {
        public static async Task EmitAsync(string outputPath)
        {
            var inputLibrary = CodeModelGenerator.Instance.InputLibrary;
            var rootNs = CodeModelGenerator.Instance.TypeFactory.PrimaryNamespace;
            var modelsNs = $"{rootNs}.Models";
            var controllersNs = $"{rootNs}.Controllers";

            foreach (var client in EnumerateClients(inputLibrary.InputNamespace.RootClients))
            {
                if (client.Methods.Count == 0)
                {
                    continue;
                }

                var source = BuildControllerSource(client, modelsNs, controllersNs);
                var path = Path.Combine(outputPath, "src", "Generated", "Controllers", $"{client.Name}ControllerBase.cs");
                Directory.CreateDirectory(Path.GetDirectoryName(path)!);
                await File.WriteAllTextAsync(path, source);
            }
        }

        private static IEnumerable<InputClient> EnumerateClients(IEnumerable<InputClient> roots)
        {
            foreach (var client in roots)
            {
                yield return client;
                foreach (var child in EnumerateClients(client.Children))
                {
                    yield return child;
                }
            }
        }

        private static string BuildControllerSource(InputClient client, string modelsNs, string controllersNs)
        {
            var sb = new StringBuilder();
            sb.AppendLine("// <auto-generated/>");
            sb.AppendLine();
            sb.AppendLine("#nullable enable");
            sb.AppendLine();
            sb.AppendLine("using System;");
            sb.AppendLine("using System.Collections.Generic;");
            sb.AppendLine("using System.Threading;");
            sb.AppendLine("using System.Threading.Tasks;");
            sb.AppendLine("using Microsoft.AspNetCore.Mvc;");
            sb.AppendLine($"using {modelsNs};");
            sb.AppendLine();
            sb.AppendLine($"namespace {controllersNs}");
            sb.AppendLine("{");

            var doc = !string.IsNullOrEmpty(client.Doc) ? client.Doc : client.Summary;
            if (!string.IsNullOrEmpty(doc))
            {
                sb.AppendLine("    /// <summary>");
                foreach (var line in SplitDocLines(doc))
                {
                    sb.Append("    /// ").AppendLine(line);
                }
                sb.AppendLine("    /// </summary>");
            }

            sb.AppendLine("    [ApiController]");
            sb.AppendLine($"    public abstract partial class {client.Name}ControllerBase : ControllerBase");
            sb.AppendLine("    {");

            for (int i = 0; i < client.Methods.Count; i++)
            {
                AppendMethod(sb, client, client.Methods[i]);
                if (i < client.Methods.Count - 1)
                {
                    sb.AppendLine();
                }
            }

            sb.AppendLine("    }");
            sb.AppendLine("}");
            return sb.ToString();
        }

        private static void AppendMethod(StringBuilder sb, InputClient client, InputServiceMethod method)
        {
            var op = method.Operation;
            var doc = !string.IsNullOrEmpty(method.Documentation) ? method.Documentation : method.Summary;
            if (!string.IsNullOrEmpty(doc))
            {
                sb.AppendLine("        /// <summary>");
                foreach (var line in SplitDocLines(doc))
                {
                    sb.Append("        /// ").AppendLine(line);
                }
                sb.AppendLine("        /// </summary>");
            }

            var verbAttr = HttpVerbAttribute(op.HttpMethod);
            var route = NormalizeRoute(op.Path);
            sb.AppendLine($"        [{verbAttr}(\"{route}\")]");

            // DEBUG removed

            var methodName = NameUtilities.ToPascalCase(method.Name);
            var returnType = BuildReturnType(method);
            var parameters = BuildParameterList(client, method);
            sb.AppendLine($"        public abstract {returnType} {methodName}Async({parameters});");
        }

        private static string HttpVerbAttribute(string httpMethod) => httpMethod.ToUpperInvariant() switch
        {
            "GET" => "HttpGet",
            "PUT" => "HttpPut",
            "POST" => "HttpPost",
            "DELETE" => "HttpDelete",
            "PATCH" => "HttpPatch",
            "HEAD" => "HttpHead",
            "OPTIONS" => "HttpOptions",
            _ => "HttpGet",
        };

        private static string NormalizeRoute(string? path)
        {
            if (string.IsNullOrEmpty(path))
            {
                return string.Empty;
            }
            // ASP.NET route templates do not start with a leading slash inside [Http*("...")].
            return path!.TrimStart('/');
        }

        private static string BuildReturnType(InputServiceMethod method)
        {
            var responseType = method.Response?.Type;
            var csharp = responseType is null
                ? null
                : CodeModelGenerator.Instance.TypeFactory.CreateCSharpType(responseType);

            if (csharp is null)
            {
                return "Task<IActionResult>";
            }

            return $"Task<ActionResult<{FormatType(csharp)}>>";
        }

        private static string BuildParameterList(InputClient client, InputServiceMethod method)
        {
            var parts = new List<string>();
            var seen = new HashSet<string>();

            // Op-level parameters cover the entire wire request, including client-scoped
            // path params (e.g., subscriptionId) and api-version. We classify by parameter
            // subtype rather than by InputMethodParameter.Location because op params are
            // typed as InputPathParameter/InputQueryParameter/InputHeaderParameter/etc.
            foreach (var p in method.Operation.Parameters)
            {
                if (p.IsApiVersion)
                {
                    continue;
                }

                var (location, isRoute) = ClassifyParameter(p);
                if (location is null)
                {
                    continue;
                }

                // accept/content-type are handled by ASP.NET content negotiation, not as method args.
                if (p is InputHeaderParameter && IsContentNegotiationHeader(p.SerializedName ?? p.Name))
                {
                    continue;
                }

                var csharp = CodeModelGenerator.Instance.TypeFactory.CreateCSharpType(p.Type);
                if (csharp is null)
                {
                    continue;
                }
                if (!p.IsRequired)
                {
                    csharp = csharp.WithNullable(true);
                }

                var name = SafeIdentifier(p.Name);
                if (!seen.Add(name))
                {
                    continue;
                }

                var binding = location switch
                {
                    InputRequestLocation.Path => $"[FromRoute(Name = \"{p.SerializedName ?? p.Name}\")]",
                    InputRequestLocation.Query => $"[FromQuery(Name = \"{p.SerializedName ?? p.Name}\")]",
                    InputRequestLocation.Header => $"[FromHeader(Name = \"{p.SerializedName ?? p.Name}\")]",
                    InputRequestLocation.Body => "[FromBody]",
                    _ => string.Empty,
                };

                var typeStr = FormatType(csharp);
                parts.Add($"{binding} {typeStr} {name}".Trim());
            }

            parts.Add("CancellationToken cancellationToken = default");
            return string.Join(", ", parts);
        }

        /// <summary>
        /// Formats a <see cref="CSharpType"/> as a short C# source string suitable for
        /// embedding in generated code. Unlike <see cref="CSharpType.ToString"/>, this
        /// emits unqualified names (relying on the <c>using</c> directives we add at
        /// the top of every controller file) and uses idiomatic suffixes such as
        /// <c>?</c> for nullables and <c>[]</c> for arrays.
        /// </summary>
        private static string FormatType(CSharpType type)
        {
            if (type.IsArray)
            {
                return FormatType(type.ElementType) + "[]";
            }

            var sb = new StringBuilder();
            sb.Append(FormatTypeName(type));
            if (type.IsGenericType && type.Arguments.Count > 0)
            {
                sb.Append('<');
                for (int i = 0; i < type.Arguments.Count; i++)
                {
                    if (i > 0) sb.Append(", ");
                    sb.Append(FormatType(type.Arguments[i]));
                }
                sb.Append('>');
            }
            if (type.IsNullable)
            {
                sb.Append('?');
            }
            return sb.ToString();
        }

        private static string FormatTypeName(CSharpType type)
        {
            if (type.IsFrameworkType && type.FrameworkType is { } ft)
            {
                // Use C# language keywords for primitives where possible.
                if (ft == typeof(string)) return "string";
                if (ft == typeof(bool)) return "bool";
                if (ft == typeof(int)) return "int";
                if (ft == typeof(long)) return "long";
                if (ft == typeof(short)) return "short";
                if (ft == typeof(byte)) return "byte";
                if (ft == typeof(float)) return "float";
                if (ft == typeof(double)) return "double";
                if (ft == typeof(decimal)) return "decimal";
                if (ft == typeof(object)) return "object";
                if (ft == typeof(void)) return "void";
            }
            return type.Name;
        }

        private static (InputRequestLocation? Location, bool IsRoute) ClassifyParameter(InputParameter p) => p switch
        {
            InputPathParameter => (InputRequestLocation.Path, true),
            InputQueryParameter => (InputRequestLocation.Query, false),
            InputHeaderParameter => (InputRequestLocation.Header, false),
            InputBodyParameter => (InputRequestLocation.Body, false),
            InputMethodParameter mp => mp.Location switch
            {
                InputRequestLocation.Path => (InputRequestLocation.Path, true),
                InputRequestLocation.Query => (InputRequestLocation.Query, false),
                InputRequestLocation.Header => (InputRequestLocation.Header, false),
                InputRequestLocation.Body => (InputRequestLocation.Body, false),
                _ => (null, false),
            },
            _ => (null, false),
        };

        private static bool IsContentNegotiationHeader(string name)
        {
            return string.Equals(name, "accept", StringComparison.OrdinalIgnoreCase)
                || string.Equals(name, "content-type", StringComparison.OrdinalIgnoreCase)
                || string.Equals(name, "contenttype", StringComparison.OrdinalIgnoreCase);
        }

        private static IEnumerable<InputParameter> EnumerateClientParameters(InputClient client)
        {
            for (var c = client; c is not null; c = c.Parent!)
            {
                foreach (var p in c.Parameters)
                {
                    yield return p;
                }
            }
        }

        private static IEnumerable<string> SplitDocLines(string? doc)
        {
            if (string.IsNullOrEmpty(doc))
            {
                yield break;
            }
            foreach (var line in doc!.Split('\n'))
            {
                yield return line.TrimEnd('\r');
            }
        }

        private static readonly HashSet<string> CSharpKeywords = new()
        {
            "abstract","as","base","bool","break","byte","case","catch","char","checked","class","const",
            "continue","decimal","default","delegate","do","double","else","enum","event","explicit",
            "extern","false","finally","fixed","float","for","foreach","goto","if","implicit","in","int",
            "interface","internal","is","lock","long","namespace","new","null","object","operator","out",
            "override","params","private","protected","public","readonly","ref","return","sbyte","sealed",
            "short","sizeof","stackalloc","static","string","struct","switch","this","throw","true","try",
            "typeof","uint","ulong","unchecked","unsafe","ushort","using","virtual","void","volatile","while"
        };

        private static string SafeIdentifier(string name)
        {
            if (string.IsNullOrEmpty(name))
            {
                return "_arg";
            }
            return CSharpKeywords.Contains(name) ? "@" + name : name;
        }
    }
}
